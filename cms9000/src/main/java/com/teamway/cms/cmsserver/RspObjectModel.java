package com.teamway.cms.cmsserver;

import com.google.protobuf.MessageLite;

public class RspObjectModel {
	private	int type;
	public RspObjectModel(int type, MessageLite msg) {
		this.type = type;
		this.msg = msg;
	}
	private	MessageLite msg;
	public int getType() {
		return type;
	}
	public void setType(int type) {
		this.type = type;
	}
	public MessageLite getMsg() {
		return msg;
	}
	public void setMsg(MessageLite msg) {
		this.msg = msg;
	}
}
