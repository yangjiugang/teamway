package com.teamway.cms.model;

import java.util.Date;

public class CameraPresetModel {
    private Long id;
    private String  name;
    private String camera_code;
    private int channel;
    private String preset_name;
    private Integer preset_index;
    private Date gmt_create;
    private Date gmt_modified;
    private String remark;
    
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getCamera_code() {
		return camera_code;
	}
	public void setCamera_code(String camera_code) {
		this.camera_code = camera_code;
	}
	public String getPreset_name() {
		return preset_name;
	}
	public void setPreset_name(String preset_name) {
		this.preset_name = preset_name;
	}
	public Integer getPreset_index() {
		return preset_index;
	}
	public void setPreset_index(Integer preset_index) {
		this.preset_index = preset_index;
	}
	public Date getGmt_create() {
		return gmt_create;
	}
	public void setGmt_create(Date gmt_create) {
		this.gmt_create = gmt_create;
	}
	public Date getGmt_modified() {
		return gmt_modified;
	}
	public void setGmt_modified(Date gmt_modified) {
		this.gmt_modified = gmt_modified;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	public int getChannel() {
		return channel;
	}
	public void setChannel(int channel) {
		this.channel = channel;
	}

}
